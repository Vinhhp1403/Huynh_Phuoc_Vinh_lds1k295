so1=float(input('nhập số thứ 1: '))
so2=float(input('nhập số thứ 2: '))
print('tổng: %.3f'%(so1+so2 ))
print('tổng: %.3f'%(so1-so2 ))
print('tổng: %.3f'%(so1*so2 ))
print('tổng: %.3f'%(so1/so2 ))
print('{:.3f}+{:.3f}={:.3f}'.format(so1,so2,so1+so2))
print('{:.3f}-{:.3f}={:.3f}'.format(so1,so2,so1-so2))
print('{:.3f}X{:.3f}={:.3f}'.format(so1,so2,so1*so2))
print('{:.3f}:{:.3f}={:.3f}'.format(so1,so2,so1/so2))
